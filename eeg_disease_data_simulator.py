def inject_ad_pattern(eeg_data, t, fs, severity="Moderate"):
    """
    Injects Alzheimer-specific EEG alterations into EEG data.
    - Decrease Alpha (8–12 Hz)
    - Increase Theta (4–7 Hz)
    - Affects parietal and occipital channels only
    """
    modified_eeg = {}
    alpha_band = (8, 12)
    theta_band = (4, 7)

    # Define severity scale
    alpha_scale = {"Mild": 0.9, "Moderate": 0.7, "Severe": 0.5}
    theta_scale = {"Mild": 1.1, "Moderate": 1.4, "Severe": 1.8}

    for ch, signal in eeg_data.items():
        modified = np.copy(signal)
        if ch in ['P3', 'P4', 'Pz', 'O1', 'O2']:
            alpha = np.sin(2 * np.pi * np.random.uniform(*alpha_band) * t) * alpha_scale[severity]
            theta = np.sin(2 * np.pi * np.random.uniform(*theta_band) * t) * theta_scale[severity]
            modified += theta
            modified *= alpha_scale[severity]
        modified_eeg[ch] = modified

    return modified_eeg


# Global artifact generator
def generate_global_artifacts(t, fs):
    blink = sum([np.exp(-((t - bt) ** 2) / 0.01) * 2.5 for bt in np.random.uniform(0.5, 3.5, size=3)])
    drift = np.zeros_like(t)
    for i in range(0, len(t), fs * 2):
        freq = np.random.uniform(0.1, 0.5)
        drift[i:i + fs * 2] += np.sin(2 * np.pi * freq * t[i:i + fs * 2]) * 0.6
    line = np.zeros_like(t)
    line_mask = (t > 1.5) & (t < 2.5)
    line[line_mask] += 0.25 * np.sin(2 * np.pi * 50 * t[line_mask])
    return {"eye_blink": blink, "nonstationary_drift": drift, "line_noise": line}

# Channel-specific noise
def add_advanced_noise(signal, fs, t, ch_name, global_artifacts):
    signal += global_artifacts["eye_blink"] + global_artifacts["nonstationary_drift"] + global_artifacts["line_noise"]
    if ch_name in ["Fp1", "Fp2"]:
        emg = np.sin(2 * np.pi * np.random.uniform(80, 120) * t) * np.random.normal(0, 0.8, len(t))
        signal += emg * ((t > 1) & (t < 2))
    else:
        if np.random.rand() < 0.5:
            emg = np.sin(2 * np.pi * np.random.uniform(60, 100) * t) * np.random.normal(0, 0.4, len(t))
            signal += emg * ((t > 1) & (t < 2))
    return signal

# EEG Generator
def generate_eeg(channels, fs, duration, stimulus):
    eeg_data = {}
    t = np.linspace(0, duration, int(fs * duration))
    global_artifacts = generate_global_artifacts(t, fs)
    phase_base = np.sin(2 * np.pi * 6 * t + np.pi / 4 * np.random.randn())

    for ch in channels:
        signal = np.random.normal(0, 0.2, len(t))
        if ch in ['F3', 'F4', 'P3', 'P4'] and np.random.rand() > 0.3:
            signal += phase_base
        if stimulus in ['visual_oddball', 'auditory_oddball'] and ch in ['Pz', 'Cz', 'Fz']:
            erp_t = 1.5
            erp_wave = np.exp(-((t - erp_t) ** 2) / 0.005) * 3.0
            signal += erp_wave
        eeg_data[ch] = add_advanced_noise(signal, fs, t, ch, global_artifacts)
    return t, eeg_data

